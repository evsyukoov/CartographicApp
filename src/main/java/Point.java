public class Point {
    String  name;
    double  x;
    double  y;
    double  h;

    public Point(String name, double x, double y, double h) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.h = h;
    }

    public Point(String name, double x, double y) {
        this.name = name;
        this.x = x;
        this.y = y;
    }


}
